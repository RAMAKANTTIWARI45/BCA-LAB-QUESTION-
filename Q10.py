# String operations program

# Taking input string
s = input("Enter a string: ")

# Slicing (example: first 5 characters)
slice_str = s[0:5]

# Concatenation (adding another string)
concat_str = s + " - Python"

# Reversing the string
reverse_str = s[::-1]

# Display results
print("\nOriginal String:", s)
print("Sliced String (first 5 chars):", slice_str)
print("Concatenated String:", concat_str)
print("Reversed String:", reverse_str)