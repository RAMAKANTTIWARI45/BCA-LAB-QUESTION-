# Built-in functions on List
my_list = [10, 20, 30, 40, 50]

print("List:", my_list)
print("Length of list:", len(my_list))
print("Maximum value:", max(my_list))
print("Minimum value:", min(my_list))
print("Sum of elements:", sum(my_list))

# Built-in functions on String
my_string = "Python Programming"

print("\nString:", my_string)
print("Length of string:", len(my_string))
print("Uppercase:", my_string.upper())
print("Lowercase:", my_string.lower())
print("Count of 'o':", my_string.count('o'))
print("Reversed string:", my_string[::-1])

# Built-in functions on Dictionary
my_dict = {'a': 1, 'b': 2, 'c': 3}

print("\nDictionary:", my_dict)
print("Length of dictionary:", len(my_dict))
print("Keys:", my_dict.keys())
print("Values:", my_dict.values())
print("Items:", my_dict.items())

# Using built-in functions with dictionary values
print("Maximum value:", max(my_dict.values()))
print("Minimum value:", min(my_dict.values()))
print("Sum of values:", sum(my_dict.values()))