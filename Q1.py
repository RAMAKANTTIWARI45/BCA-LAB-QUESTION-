#types conversion

a = 10
print("Integer value:", a)
print("Type:", type(a))

b = 5.5
print("\nFloat value:", b)
print("Type:", type(b))

c = float(a)
print("\nAfter converting int to float:", c)
print("Type:", type(c))

d = int(b)
print("\nAfter converting float to int:", d)
print("Type:", type(d))